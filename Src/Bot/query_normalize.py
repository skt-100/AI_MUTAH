import re

# كلمات لا فائدة منها في البحث
STOP_WORDS = {
    # أدوات الاستفهام
    "ما", "هو", "هي", "هم", "من", "في", "على", "إلى", "مع",
    "هل", "كيف", "ماذا", "متى", "اين", "أين", "لماذا", "كم",
    # حروف وأدوات
    "ال", "و", "أو", "لكن", "إن", "أن", "كان", "يكون", "لل",
    # ألقاب كاملة تُحذف لأن FACULTY_KEYWORDS تحتوي على الجذر فقط
    "للدكتور", "للأستاذ", "للدكتورة", "للأستاذة",
    # أفعال الطلب
    "اريد", "أريد", "ابغى", "أبغى", "عايز",
    "اعطني", "أعطني", "قولي", "خبرني", "اسمع",
    # كلمات مبهمة أو زائدة
    "معلومات", "عن", "بخصوص", "حول", "طيب",
    "وشو", "وش", "شو", "ايش", "وعلم",
    # كلمات إلكترونية مركّبة (البريد وحده يكفي للبحث)
    "الالكتروني", "الألكتروني", "الكتروني", "ألكتروني",
    "ماهو", "ماهي", "هاتف",
}

def normalize_query(question):
    # 1. إزالة التشكيل
    question = re.sub(r'[\u0617-\u061A\u064B-\u0652]', '', question)

    # 2. توحيد الاحرف
    question = re.sub(r'[أإآا]', 'ا', question)
    question = re.sub(r'[ة]', 'ه', question)

    # 3. إزالة الرموز والأرقام
    question = re.sub(r'[^\w\s]', '', question)


    # 4. تقسيم السؤال إلى كلمات
    words = question.split()

    # 5. إزالة الـ stop words
    words = [word for word in words if word not in STOP_WORDS]

    # 6. إرجاع السؤال نظيفاً
    clean_question = " ".join(words)
    return clean_question


# تجربة
if __name__ == "__main__":
    questions = [
        "ما هو البريد الالكتروني للدكتور سائد القرالة؟",
        "من هم أساتذة قسم علم الحاسوب؟",
        "أريد معلومات عن الدكتور خالد الطراونة"
    ]
    for q in questions:
        print(f"قبل: {q}")
        print(f"بعد: {normalize_query(q)}")
        print()
